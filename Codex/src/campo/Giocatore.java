package campo;

public class Giocatore {
	
		//Attributi
		private String nome;
		int punti;

		//Costrutto
		public Giocatore(String nome) {
			this.nome=nome;
		}
		
		public String getNome() {
			return nome;
		}
	
		public String setNome() {
			return nome;
		}
		
		public String getPunti() {
			return nome;
		}
		
		public int calolaPunti() {
			return punti;	
		}
}
